
public class ExemploBreakContinue {
    
    public static void main(String[] args) {
        // utilizamos o break quando queremos parar a execução e continue para 

        for (int i = 0; i <=5; i++){
            if(i == 3){
                break; // vai parar o codigo quando a execução for verdadeira// 0 continue tmb é assi, ==m

            }
            System.out.println("Numero: " + i);
        }
    }


}
